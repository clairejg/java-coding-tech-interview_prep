public class printHello {
//   Access specifier, Class declaration, classNme
//    접근권한, 클래스 선언, 클래스 이름
//    자바에서는 보통 하나의 파일에 하나의 class를 작성합니다.
//    public 이 붙어 있는 클래스일 경우에 반드시 클래스 이름이 파일 이름이 되어야 합니다
    public static void main(String[] args) {
        System.out.println("Hello");
        //이 부분은 항상 똑같은 모양을 가집니다.
        // Java Virtual Machine(자바 가상 기계)은 프로그램이 로드된후 main() 메소드를 찾아서 이부분에서 프로그램을 시작합니다.
    }
}


